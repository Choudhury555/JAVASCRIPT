//ARRAY PRACTICE
let fruits = ["apple","banana","mango"];

console.log(fruits[0]);//apple

fruits.push("orange");
console.log(fruits);//[ 'apple', 'banana', 'mango', 'orange' ]

fruits.pop();
console.log(fruits);//[ 'apple', 'banana', 'mango' ]

fruits.unshift("grape");
console.log(fruits);//[ 'grape', 'apple', 'banana', 'mango' ]

fruits.shift();
console.log(fruits);//[ 'apple', 'banana', 'mango' ]

